// Cara yang salah saat memecahkan masalah
/*
    > Lompat langsung ke masalah tanpa banyak berpikir
    > Mengimplementasikan solusi dengan cara yang tidak terstruktur tanpa pendektatan yang logis
    > Dapat mudah stress ketika segala sesuatunya tidak segera berhasil
    > Terlalu bangga untuk reserach solusi ketika tidak tau bagaimana menemukan solusinya sendiri
*/

//////////////////////////////////////////
// Cara yang benar saat memecahkan masalah
/*
    > Tenang dan santai, jangan langsung melompat pada masalah tanpa mempuyai rencana
    > Memiliki pola pikir untuk mengambil pendekatan logis dan rasional (pada dasarnya pemrogramman hanyalah logika)
    > Menggunakan 4 langkah kerangka kerja untuk menyelesaikan masalah
        - 1. Memastikan bahwa kita 100% memahami masalahnya. (cara terbaik adalah membuat pertanyaan untuk mendapatkan gambaran besar dari masalahnya)
        - 2. Strategi membagi dan menaklukan. (membagi masalah yang besar menjadi sebanyak mungkin masalah yang kecil, karna masalah kecil lebih mudah dipecahkan)
        - 3. Jangan takut melakukan reserach sebanyak yang kita harus lakukan
        - 4. Untuk memecahkan masalah yang lebih besar, kita harus menggabungkan semeua langkah sebelumnya dan menulis pseudo-code sebelum menulis kode yang sebenarnya
*/

