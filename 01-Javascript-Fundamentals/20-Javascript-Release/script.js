// Sejarah javascript

/*

JavaScript pertama kali muncul pada tahun 1995 sebagai bahasa pemrograman untuk web browser. Berikut adalah sejarah singkat tentang JavaScript:

1995 - Kelahiran JavaScript:

Brendan Eich dari Netscape Communications menciptakan bahasa ini dalam waktu 10 hari. Awalnya disebut Mocha, kemudian berganti nama menjadi LiveScript, dan akhirnya menjadi JavaScript.
Netscape merilisnya sebagai bagian dari Netscape Navigator 2.0 pada bulan Desember 1995.
1996 - Standarisasi Awal:

Microsoft menciptakan versi mereka sendiri dari JavaScript, yang disebut JScript, untuk Internet Explorer.
Netscape menyerahkan JavaScript kepada ECMA International untuk standarisasi. Proses ini menghasilkan ECMAScript sebagai standar resmi.
1997 - ECMAScript 1:

ECMAScript 1 (ES1) menjadi standar pertama untuk JavaScript, dirilis oleh ECMA International.
1999 - ECMAScript 3:

ECMAScript 3 (ES3) membawa banyak fitur baru dan perbaikan, menjadi versi yang paling banyak digunakan selama bertahun-tahun.
2009 - ECMAScript 5:

ECMAScript 5 (ES5) dirilis dengan tambahan fitur seperti mode strict, JSON, dan metode array baru. Versi ini juga meningkatkan kemampuan bahasa secara keseluruhan.
2015 - ECMAScript 6 (ES6) / ECMAScript 2015:

ES6, juga dikenal sebagai ECMAScript 2015, memperkenalkan banyak fitur baru yang signifikan, termasuk sintaks baru seperti let dan const, class, modules, arrow functions, promises, dan banyak lagi. ES6 membawa JavaScript ke tingkat baru dalam hal kemampuan dan fleksibilitas.
Setelah 2015 - Pembaruan Tahunan:

Sejak ES6, ECMAScript mengalami pembaruan tahunan dengan fitur-fitur baru yang terus ditambahkan setiap tahun. Beberapa pembaruan penting termasuk async/await (ECMAScript 2017), dynamic import (ECMAScript 2020), dan banyak lagi.
JavaScript kini menjadi salah satu bahasa pemrograman paling populer di dunia, digunakan tidak hanya untuk pengembangan front-end tetapi juga untuk back-end (misalnya dengan Node.js), aplikasi seluler, dan bahkan aplikasi desktop.

*/

// ES6+ vs ESNext

/*

JavaScript modern dan ESNext adalah istilah yang digunakan untuk menggambarkan perkembangan terbaru dan fitur terbaru dalam bahasa pemrograman JavaScript.

JavaScript Modern
JavaScript modern merujuk pada versi dan praktik JavaScript yang telah berkembang sejak rilis ECMAScript 2015 (ES6) dan seterusnya. Beberapa fitur utama yang memperkenalkan JavaScript modern meliputi:

Sintaks baru dan peningkatan:

let dan const untuk mendeklarasikan variabel dengan lingkup blok.
Arrow functions (() => {}) yang menyediakan sintaks yang lebih ringkas untuk fungsi.
Template literals (`Hello, ${name}!`) untuk string interpolasi.
Destructuring assignment untuk mengekstrak data dari array atau objek.
Rest parameters (...args) dan spread operator (...array).
Fitur baru dalam bahasa:

Classes dan inheritance yang menyediakan cara lebih formal untuk bekerja dengan objek dan pewarisan.
Modules (import dan export) untuk pengelolaan kode modular.
Promises dan async/await untuk penanganan asynchronous yang lebih sederhana dan lebih mudah dibaca.
Default parameters untuk fungsi.
API dan objek built-in baru:

Map, Set, WeakMap, dan WeakSet untuk koleksi data yang lebih fleksibel.
Symbol sebagai tipe data primitif baru.
Proxy dan Reflect untuk pengawasan dan pengendalian objek.
ESNext
ESNext adalah istilah yang merujuk pada fitur dan proposal yang sedang dalam proses untuk ditambahkan ke spesifikasi ECMAScript di masa depan. ESNext mencakup semua fitur baru yang belum secara resmi dirilis dalam versi ECMAScript yang diterima, tetapi sedang dipertimbangkan atau diimplementasikan dalam browser modern.

Beberapa fitur ESNext yang sedang dikembangkan atau baru-baru ini ditambahkan meliputi:

Optional chaining (?.), yang memungkinkan akses properti dan pemanggilan metode dengan aman pada objek yang mungkin undefined atau null.
Nullish coalescing (??), yang memberikan nilai default saat operand adalah null atau undefined.
Logical assignment operators (&&=, ||=, ??=) untuk pengoperasian logis dan penugasan dalam satu langkah.
Top-level await yang memungkinkan penggunaan await di luar fungsi async.
Private class fields yang memungkinkan deklarasi properti privat dalam kelas menggunakan #.
Rilis Tahunan ECMAScript
Sejak ES6, komite TC39 yang mengawasi pengembangan ECMAScript telah mengadopsi siklus rilis tahunan, dengan pembaruan yang dilakukan setiap tahun. Fitur-fitur baru diperkenalkan berdasarkan proposal yang melalui beberapa tahap diskusi dan persetujuan. Hal ini memastikan bahwa JavaScript terus berkembang dan memperkenalkan fitur-fitur yang memperbaiki kemampuan dan pengalaman pengembang.

Dengan mengadopsi praktik dan fitur dari JavaScript modern serta tetap mengikuti perkembangan ESNext, pengembang dapat menulis kode yang lebih efisien, lebih mudah dipelihara, dan lebih kuat.

*/
