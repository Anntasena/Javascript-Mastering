//! First Class VS. Higher-Order Functions
//? JS adalah bahasa yang mempunyai "first class function"
/*
#-> First class functions
  *-> secara teknis JS menyebutnya "first-class citizens"
  *-> artinya function hanya diperlakukan sebagai "simply value"
  *-> function sebenarnya hanyalah "type of object" di JS
  $ [FACT] karna function adalah object of values, function adalah values juga. Oleh sebab itu banyak hal menarik yang bisa dilakukan, seperti:
    - menyimpan variable
    - menjadi object properties
    - meneruskan function sebagai argumen ke function lain
    - dapat me-RETURN function ke function lain
    - function dapat menggunakan method

#-> Higher-order functions
  *-> function yang menerima function lain sebagai argument, atau function yang mengembalikan function baru
  *-> ini hanya bisa terjadi karna ada first-class function
*/



